import java.io.Serializable;

/**
 *
 * @author tvanrossum
 */
public class ShoppingListSubject implements Serializable {
    private Long    listID;
    private Integer subjectID;

    /**
     * @return the listID
     */
    public Long getListID() {
        return listID;
    }

    /**
     * @param listID the listID to set
     */
    public void setListID(Long listID) {
        this.listID = listID;
    }

    /**
     * @return the subjectContentsID
     */
    public Integer getSubjectID() {
        return subjectID;
    }

    /**
     * @param subjectContentsID the subjectContentsID to set
     */
    public void setSubjectID(Integer subjectID) {
        this.subjectID = subjectID;
    }
}



